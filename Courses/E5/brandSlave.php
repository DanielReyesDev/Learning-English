		<h2 align="center">Are you a "brand slave" ?</h2><br><br><br>

		<p>12A Read the text.</p>

		<p>Designer brands, such as Nike, Gap or even Virgin, are a major part of modern life. Often the brands are global names. But why do some people think that brands are important, while others <span style="color:red;">could not care less</span> about them?
		Here a group of British teenagers talk about the importance - or unimportance - of wearing designer clothes.</p><br><br>

		<p><b>MIKE:</b> Yes, I think that brands are quite important. When I buy clothes, I tend to go for good brands, like Nike or Gap, 'cos you know they're good quality.</p><br>

		<p><b>TESSA:</b> Well I think that's stupid, I really do. The only time I ever buy Nike or things like that is in the sales, when they're cheap. Otherwise, designer brands are a complete rip-off as far as I'm concerned. I can't afford to pay twice as much as I need to, just to have Nike or CK. It's a total waste of money!</p><br>

		<p><b>MIKE:</b> But it isn't, <span class="yellow-highlight">is it?</span> I mean, you know you're getting good quality if you buy a well-known brand. </p><br>

		<p><b>TESSA:</b> Maybe, but that doesn't mean the opposite's true, <span class="yellow-highlight">does it?</span> I mean, when you buy a Nike baseball cap or something, you're really paying about forty quid just to wear the logo! You can get perfectly good quality for half the price without wasting your money on designer stuff, <span class="yellow-highlight">can't you?</span></p><br>

		<p><b>MIKE:</b> Sometimes, yeah; but you can't be sure, <span class="yellow-highlight"> can you?</span> I mean, if you buy a pair of Nike trainers, you know you've got a good pair of shoes, and everyone else knows it too.</p><br>

		<p><b>TESSA:</b> Do they? I think that's just silly! Frankly, I don't go round looking to see <span class="blue-highlight">what shoes people have got on</span>; and I certainly wouldn't judge a person by their clothes.</p><br>

		<p><b>SARAH:</b> It's not a matter of judging people by their clothes. But I think, like, you feel better if you know you've got good clothes <span class="blue-highlight">on</span>. Not necessarily things like Gap, but anything with a reasonably good label. I mean, lots of people <span class="blue-highlight">wouldn't be seen dead</span> wearing stuff from Littlewoods or Marks & Sparks — it's just so un-cool.</p><br>

		<p><b>MIKE:</b> Yeah, it's all about image, <span class="yellow-highlight">isn't it?</span></p><br>

		<p><b>TESSA:</b> Exactly, <span class="blue-highlight">you've hit the nail on the head</span>, <span class="yellow-highlight">haven't you?</span> It's just image. I mean, you buy designer brands just because of the image. Not for any real reason at all. Just image, or imagination. You imagine peoplethink more of you 'cos you're wearing Armani jeans or something like that.</p><br>

		<p><b>MIKE:</b> Well they do. That's why people wear them, <span class="yellow-highlight">isn't it? </span> I mean, when you go anywhere, people judge you by what they can see, don't they? If you're wearing crap clothes, no one's going to think much of you at all!</p><br>

		<p><b>TOM:</b> I think you're missing the point there, Mike; after all it's not a question of choosing between designer clothes and crappy clothes, it's a matter of choosing between good expensive clothes with a posh label, and cheaper good clothes without one. You can perfectly well wear good clothes that don't have some fancyname. I mean, look at me!  Are you going to look down on me, 'cos there aren't any trendy labels on any of my gear?</p><br>

		<p><b>SARAH:</b> No, not really, but if you had on a Calvin Klein sweatshirt or something, I might think you were cooler.</p><br>

		<p><b>TOM:</b> Oh that's really gross!</p><br>

		<p><b>TESSA:</b> I call it juvenile.</p><br>

		<p><b>SARAH:</b> But it isn't! Like Tom said, people do judge you by what they see, and if they see you're wearing good clothes, they'll be more likely to get a good impression of you.</p><br>

		<p><b>TOM:</b> But the things I'm wearing are perfectly good, <span class="yellow-highlight">aren't they?</span></p><br>

		<p><b>SARAH:</b> They're O.K?..</p><br>

		<p><b>TOM:</b> They're perfectly good, and good quality too. I can't see what makes Mike's sweatshirt any better than mine, just 'cos it's got GAP written <span class="blue-highlight">on it</span>. Besides, half of designer branded stuff is made usingsweatshop labour in Asia. You know there's kids of ten years old working in factories in places like Cambodia, making cheap designer stuff for famous brands. There was a documentary on TV the other day.</p><br>

		<p><b>TESSA:</b> Yes, like, you buy a pair of super trainers for £100 here, and the people who actually made them were being paid 50p an hour or something. Your posh expensive trainers really cost about £5 to make. It's no wonder that big fashion companies can afford all their expensive advertising, no wonder their bosses are millionaires.</p><br>

		<p><b>SARAH:</b> Yeah, maybe, but it's the same for everything, <span class="yellow-highlight">isn't it?</span> It isn't just the big names that are exploiting people in poor countries, <span class="yellow-highlight">is it?</span> They're all doing it. I mean look at your shoes, Tom. Where were they made?</p><br>

		<p><b>TOM:</b> I haven't a clue.</p><br>

		<p><b>SARAH:</b> No, so how do you know they weren't made using child labour too? You don't, <span class="yellow-highlight">do you?</span></p><br>

		<p><b>TOM:</b> No: but even so, <span class="blue-highlight">two wrongs don't make a right</span>. And it doesn't make any difference to the fact that lots of designer brands use sweatshop labour in Asia, <span class="yellow-highlight">does it?</span></p><br>

		<p><b>MIKE:</b> Frankly, I don't see what I can do about it, since you can't go round asking <span class="blue-highlight">where everything you buy was made</span>. It's just not practical. And wherever they're made, I still like wearing good clothes. I don't feel good in crap outfits</p><br>

		<p><b>SARAH:</b> Nor me neither.</p><br><br><br><br>

